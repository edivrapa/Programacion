public class Operaciones {
	/**
	 * 
	 * @param n1 Es el parametro numero 1 del metodo sumar 
	 * @param n2 Es el parametro numero 2 del metodo sumar 
	 * @return retorna la suma de n1 y n2
	 */
	public int sumar(int n1, int n2){
		return n1+n2;
	}
	/**
	 * 
	 * @param n1 Es el parametro numero 1 del metodo restar
	 * @param n2 Es el parametro numero 2 del metodo restar
	 * @return retorna la resta de n1 y n2
	 */
	public int restar(int n1, int n2){
		return n1-n2;
	}
	/**
	 * 
	 * @param n1 Es el parametro numero 1 del metodo multiplicar
	 * @param n2 Es el parametro numero 2 del metodo multiplicar
	 * @return retorna la multiplicación de n1 y n2
	 */
	public int multiplicar(int n1, int n2){
		return n1*n2;
	}
	/**
	 * 
	 * @param n1
	 * @param n2
	 * @return
	 */
	public int dividir (int n1, int n2){
		return n1/n2;
	}

}
